/******************************************************************************
* \Syntax          : void	Led_Init(u8 PortId, u8 PinId)

* \Description     : initiate Led pin
*                                                                             
* \Sync\Async      : Synchronous                                               
* \Reentrancy      : Re-entrant                                             
* \Parameters (in) : PortId: the port the LED is on 
										 PinId:	the pin the Led is on

* \Parameters (out): void                                                      
* \Return value:   void                                  
*******************************************************************************/


void	Led_Init(u8 PortId, u8 PinId)
{
	Port_ConfigType  Led_Pin = {PortId, PinId, PORT_PINMODE_DIO, LOW, Output, NoPinAttachType, R8R};
	Port_Init(&Led_Pin);
	
}

void  Led_ON(void)
{
	Dio_WriteChannel(LED_PORT ,LED_PIN, HIGH);
	
}

void  Led_OFF(void)
{
	Dio_WriteChannel(LED_PORT ,LED_PIN, LOW);
}

void  Led_Activate(u32 On_Time)
{
	Led_ON();
	Systick_StartTimer(On_Time);
}


